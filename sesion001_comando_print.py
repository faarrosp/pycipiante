# Bienvenidos a esta primera sesion de programacion en Python

# Nuestra primera interaccion con la consola sera imprimir un mensaje
print('Hola a todos, mi nombre es Felipe')

# como podemos observar, el comando "print" toma colores especiales
# distintos a los parentesis y al mensaje mismo
# "print" es una "palabra protegida", porque Python las interpreta
# como comandos

# otras palabras protegidas son:

# print
# int
# if
# else
# with
# class 
# def
# import
# from

# etc... 

# para poder hacer comentarios (escribir libremente en un script)
# simplemente usamos el hash o "gato"

# El comando print lo podemos utilizar con mas de un argumento. Intentemoslo

print('Hola a todos', 'mi nombre es Felipe')

# si copiamos la linea 30 en el compilador online, nos daremos cuenta que
# el mensaje se imprime introduciendo un espacio entre los dos argumentos
# este comportamiento es por defecto. Podemos agregar mas palabras y todas
# seran separadas por un espacio, sin que tengamos que introducirlo nosotros

print('Hola a todos', 'mi nombre es Felipe', 'y tengo 32 anhos')
